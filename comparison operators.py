#比较运算符在我们比较一个变量和一个值的时候被使用
#例子：
#如果温度比30度高，则输出‘今天的天气真热’
#如果温度低于10度，则输出‘今天天气很冷’
#如果以上两者都不是，则输出‘今天的天气很好’
temperature=31#输入不同的温度值会得到不同的输出结果。
if temperature>30:
    print('今天的天气很热')
elif temperature<10:
        print('今天的天气很冷')
else:
    print('今天的天气很好')
