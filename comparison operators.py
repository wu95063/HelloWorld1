##做个练习：
##用户输入名字，少于1个字的，提示信息显示：输入字最少为2个
##用户输入名字，多于15个字的，提示信息显示：输入字最多为15个。
##用户输入名字在2-15个字之间，提示信息显示：名字真棒。
name_count=input('请输入您的名字： ')
if len(name_count) <= 1:
    print("输入字数最少为2个")
elif len(name_count) >15:
    print('输入字数最多为15个')
else:
    print('您的名字真棒')
